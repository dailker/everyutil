/**
 * Logical equivalence (A iff B).
 * @author @dailker
 * @param {boolean} a - First boolean value.
 * @param {boolean} b - Second boolean value.
 * @returns {boolean} True if both values are equivalent, false otherwise.
 */
export function biconditional(a: boolean, b: boolean): boolean {
  return a === b;
}
